int	ft_atoi(char *str)
{
	int	n;
	int	s;
	int	c;

	n = 0;
	s = 1;
	c = 0;
	while ((str[c] >= 9 && str[c] <= 13) || str[c] == 32)
		c++;
	while (str[c] == '+' || str[c] == '-')
	{
		if (str[c] == '-')
			s *= -1;
		c++;
	}
	while (str[c] >= '0' && str[c] <= '9')
	{
		n = (str[c] - '0') + (n * 10);
		c++;
	}
	return (n * s);
}

