#include <unistd.h>

int	main(int ac, char *av[])
{
	int	a;
	int	b;

	a = ac - 1;
	while (a > 0)
	{
		b = 0;
		while (av[a][b] != '\0' && ac)
		{
			write(1, &av[a][b], 1);
			b = b + 1;
		}
		write(1, "\n", 1);
		a = a -1;
	}
}
